function Level1.draw(self)
    for i,row in ipairs(self.tileMap) do
      for j,tile in ipairs(row) do
        if tile == 1 then
          self.wall.draw(self.wall, (j-1) * self.wall.width, (i-1) * self.wall.height)
          table.insert(self.walls, Wall.new(Wall, (j-1) * self.wall.width, (i-1) * self.wall.height, self.wall.width, self.wall.height))
        else
          self.grass.draw(self.grass, (j-1) * self.grass.width, (i-1) * self.grass.height)
        end
      end
    end
end

function Level1.checkTileColision(self, player)
  for i, wall in pairs(self.walls) do
    if player.x - player.width / 2 <= wall.x + wall.width
    and player.x + player.width / 2 >= wall.x 
    and player.y + player.height / 2 >= wall.y
    and player.y - player.height / 2<= wall.y + wall.height then
      return true
    end
  end
  return false
end

